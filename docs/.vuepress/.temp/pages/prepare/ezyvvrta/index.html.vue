<template><div><h2 id="介绍" tabindex="-1"><a class="header-anchor" href="#介绍"><span>介绍</span></a></h2>
<p><strong>行测</strong>（行政职业能力测验）和 <strong>职测</strong>（职业能力倾向测验）是公职类考试中 <strong>必考的两种笔试科目</strong>，
都是对考生职业能力的考查，旨在测查考生在行政职业领域内的潜在能力和素质，通过不同的题目形式来评估考生是否具备从事相应工作的基本能力。</p>
<h2 id="相同点" tabindex="-1"><a class="header-anchor" href="#相同点"><span>相同点</span></a></h2>
<ul>
<li><strong>考试内容</strong>
<ul>
<li><strong>常识判断</strong>：都考查考生对政治、经济、法律、历史、文化、地理、环境、自然、科技等方面的基本知识及其运用。</li>
<li><strong>言语理解与表达</strong>：都包括对词语、句子的理解和运用，如选词填空、片段阅读等题型，考查考生对语言文字的理解和表达能力。</li>
<li><strong>数量关系</strong>：都会涉及数字推理和数学运算等内容，考查考生对数量关系的理解和计算能力，不过数字推理出现的频率相对较低。</li>
<li><strong>判断推理</strong>：都有图形推理、定义判断、类比推理、逻辑判断等题型，考查考生的逻辑思维能力、分析判断能力等。</li>
</ul>
</li>
</ul>
<h2 id="不同点" tabindex="-1"><a class="header-anchor" href="#不同点"><span>不同点</span></a></h2>
<ul>
<li><strong>考试目的</strong>
<ul>
<li><strong>行测</strong>：主要用于<strong>公务员录用考试</strong>，是为了选拔出能够胜任行政机关工作的综合性人才，侧重于考查考生的行政综合能力和基本素质。</li>
<li><strong>职测</strong>：主要用于<strong>事业单位等招聘考试</strong>，是为了选拔出适合特定事业单位岗位的专业人才，会根据不同岗位类别，在考查重点上有所差异。</li>
</ul>
</li>
<li><strong>考试题型分布</strong>
<ul>
<li><strong>行测</strong>：题型相对固定，一般包括常识判断、言语理解与表达、数量关系、判断推理和资料分析五大部分，题量通常在135题左右。</li>
<li><strong>职测</strong>：会根据不同的岗位类别分为A、B、C、D、E等五类，题型和题量会有所不同。如综合管理类（A类）与申论较为相似；社会科学专技类（B类）有综合分析题；自然科学专技类（C类）有实验设计题；中小学教师类（D类）有策略选择；医疗卫生类（E类）也有策略选择等特殊题型。</li>
</ul>
</li>
<li><strong>考试难度</strong>
<ul>
<li><strong>行测</strong>：整体难度相对较高，特别是在数量关系和判断推理部分，题目较为复杂，对考生的逻辑思维能力和计算能力要求较高，竞争也更为激烈。</li>
<li><strong>职测</strong>：难度相对较低，但具体难度也会因岗位类别和地区而异。职测更注重考查考生在特定领域的基础知识和应用能力。</li>
</ul>
</li>
<li><strong>命题特点</strong>
<ul>
<li><strong>行测</strong>：命题更加注重宏观层面的考查，强调对国家政策、社会现象等宏观内容的理解和把握，题目往往具有较强的综合性和系统性。</li>
<li><strong>职测</strong>：命题会更贴近事业单位的工作实际和专业需求，可能会涉及一些具体的专业知识和业务场景，更加注重实用性和针对性。</li>
</ul>
</li>
</ul>
